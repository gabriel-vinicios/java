
public class Veiculo {
    int Peso;
    int VelocMax;
    float Preco;

    public int getPeso() {
        return Peso;
    }

    public void setPeso(int peso) {
        Peso = peso;
    }

    public float getPreco() {
        return Preco;
    }
    public void setPreco(float preco) {
        Preco = preco;
    }
    public int getVelocMax() {
        return VelocMax;
    }
    public void setVelocMax(int velocMax) {
        VelocMax = velocMax;
    }
}